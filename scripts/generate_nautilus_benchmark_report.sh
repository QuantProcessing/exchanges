#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT_DIR"

GOCACHE="${GOCACHE:-/private/tmp/go-build-exchanges}"
export GOCACHE

REPORT="${NAUTILUS_BENCHMARK_REPORT:-.omx/reports/nautilus-benchmark-report.md}"
REPORT_DIR="$(dirname "$REPORT")"
TMP_DIR="$(mktemp -d)"
trap 'rm -rf "$TMP_DIR"' EXIT

mkdir -p "$REPORT_DIR"

cat >"$REPORT" <<'HEADER'
# Nautilus Benchmark Report

Generated by `scripts/generate_nautilus_benchmark_report.sh`.

HEADER

section_index=0

run_section() {
  local title="$1"
  local required_pattern="$2"
  shift 2

  section_index=$((section_index + 1))
  local output="$TMP_DIR/section-${section_index}.txt"
  local exit_code=0

  {
    printf '## %s\n\n' "$title"
    printf 'Command: `%s`\n\n' "$*"
    printf '```text\n'
  } >>"$REPORT"

  "$@" >"$output" 2>&1 || exit_code=$?
  cat "$output" >>"$REPORT"
  printf '```\n\n' >>"$REPORT"

  if (( exit_code != 0 )); then
    printf 'Status: FAIL (exit %d)\n\n' "$exit_code" >>"$REPORT"
    return "$exit_code"
  fi
  if ! rg -q "$required_pattern" "$output"; then
    printf 'Status: FAIL (required pattern not found: `%s`)\n\n' "$required_pattern" >>"$REPORT"
    return 1
  fi
  printf 'Status: PASS\n\n' >>"$REPORT"
}

run_section "Matching Core Benchmark" "BenchmarkMatchingCoreOrderBookDepth" \
  go test -run '^$' -bench '^BenchmarkMatchingCoreOrderBookDepth$' -benchmem -benchtime=100x ./backtest

run_section "Event Dispatch Benchmark" "BenchmarkBusPublishFanout" \
  go test -run '^$' -bench '^BenchmarkBusPublishFanout$' -benchmem -benchtime=100x ./bus

run_section "Reconciliation Benchmark" "BenchmarkReconcilerMassStatus" \
  go test -run '^$' -bench '^BenchmarkReconcilerMassStatus$' -benchmem -benchtime=100x ./execution

run_section "Adapter Fake Contract Suites" "Test.*ClientsPassVenueContractSuite" \
  go test -count=1 ./adapter/... -run 'Test.*ClientsPassVenueContractSuite' -v

printf 'Benchmark report written to %s\n' "$REPORT"
